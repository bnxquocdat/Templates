#include <bits/stdc++.h>
using namespace std;
using ll = long long; 
#define ull unsigned long long 
const int mod = 1000000;
const int MOD = 1e9+7;
unsigned long long binpow(long long a, long long b) {
    unsigned long long res = 1;
    while (b > 0) {
        if (b & 1)
            res = (res * a) % MOD;
        a = (a * a) % MOD;
        b >>= 1;
    }
    return res % MOD;
}
ull binarypow(ull n , ull m) 
{
    if (m == 0) 
    {
        return 1; 
    }
    ull rs = binarypow(n, m / 2);
    rs = (rs * rs) % MOD;
    if (m % 2 == 1) {
        rs = (rs * n) % MOD;
    }
    return rs;
}
